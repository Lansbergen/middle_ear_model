Lc = Pk.Lc;
Cc = Pk.Cc;
Rc = Pk.Rc;

% total equivalent impedance
Xl = w.*Lc;
Xc = 1./(w.*Cc);

Xtot = Xl - Xc;  
Rtot = Rc;

Z5 = complex(Rtot,Xtot);

